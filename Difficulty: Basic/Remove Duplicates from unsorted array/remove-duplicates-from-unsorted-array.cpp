class Solution {
  public:
    vector<int> removeDuplicate(vector<int>& arr) {
        std::unordered_set<int> seen;
        std::vector<int> result;

        for (int num : arr) {
            if (seen.find(num) == seen.end()) { // If not seen
                seen.insert(num);
                result.push_back(num);
            }
        }

        return result;
    }
};


